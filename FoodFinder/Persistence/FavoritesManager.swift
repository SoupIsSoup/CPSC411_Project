//===== Begin code area ====================================================================================================================================================

import Foundation
import SQLite

//==========================================================================================================================================================================
//===== FavoritesManager class =============================================================================================================================================
//==========================================================================================================================================================================

final class FavoritesManager {

    //======================================================================================================================================================================
    //===== Stored properties ==============================================================================================================================================
    //======================================================================================================================================================================

    private let db: Connection
    private let favorites = Table("favorites")

    private let id = Expression<Int64>("id")
    private let name = Expression<String>("name")
    private let category = Expression<String>("category")

    //======================================================================================================================================================================
    //===== Initialization ================================================================================================================================================
//======================================================================================================================================================================

    init() throws {

        let url = try FileManager.default
            .url(for: .documentDirectory,
                 in: .userDomainMask,
                 appropriateFor: nil,
                 create: true)
            .appendingPathComponent("favorites.sqlite3")

        db = try Connection(url.path)

        try db.run(favorites.create(ifNotExists: true) { t in
            t.column(id, primaryKey: .autoincrement)
            t.column(name)
            t.column(category)
        })
    }

    //======================================================================================================================================================================
    //===== Add favorite =================================================================================================================================================
//======================================================================================================================================================================

    func addFavorite(name: String, category: String) throws {

        let insert = favorites.insert(self.name <- name,
                                      self.category <- category)

        try db.run(insert)
    }

    //======================================================================================================================================================================
    //===== Get favorites =================================================================================================================================================
//======================================================================================================================================================================

    func getFavorites() throws -> [String] {

        try db.prepare(favorites).map { row in
            "\(row[name]) - \(row[category])"
        }
    }
}

